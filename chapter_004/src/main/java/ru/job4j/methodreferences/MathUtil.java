public class MathUtil {

    /**
     * Method add.
     *
     * @param left - first param.
     * @param second - last param.
     * @return - result addition.
     */
    public static double add(int left, int second) {
        return left + second;
    }

    /**
     * Method division.
     *
     * @param left - first param.
     * @param second - last param.
     * @return - result division.
     */
    public static double div(int left, int second) {
        return left / second;
    }
}
